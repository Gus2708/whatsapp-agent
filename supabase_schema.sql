-- 14. Suscripciones Web Push (notificaciones con la app CERRADA).
--     El emisor es la Edge Function 'send-push' + un trigger 'notify_push' (pg_net) sobre
--     solicitudes_ayuda y atenciones_pendientes, ambos desplegados en Supabase. La clave VAPID
--     privada y el secreto del trigger viven dentro de la función (NO en este repo público).
CREATE TABLE IF NOT EXISTS public.push_subscriptions (
    id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    empleado_id  uuid,
    endpoint     text NOT NULL UNIQUE,
    subscription jsonb NOT NULL,            -- objeto PushSubscription completo (endpoint + keys)
    user_agent   text,
    creado_en    timestamp with time zone NOT NULL DEFAULT now()
);
ALTER TABLE public.push_subscriptions ENABLE ROW LEVEL SECURITY;

