CREATE INDEX IF NOT EXISTS idx_chat_sessions_updated ON public.chat_sessions USING btree (updated_at);

-- 9. Función para búsqueda difusa de productos
CREATE OR REPLACE FUNCTION public.buscar_productos(
    p_busqueda text,
    p_limite integer DEFAULT 5
)
RETURNS TABLE (
    codigo_interno text,
    descripcion text,
    precio_venta numeric,
    existencia numeric,
    score float
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        p.codigo_interno,
        p.descripcion,
        p.precio_venta,
        p.existencia,
        similarity(p.descripcion, p_busqueda)::float AS score
    FROM public.productos p
    WHERE p.descripcion % p_busqueda OR p.descripcion ILIKE ('%' || p_busqueda || '%')
    ORDER BY score DESC, p.existencia DESC
    LIMIT p_limite;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 10. Función para auditoría de movimientos de stock
CREATE OR REPLACE FUNCTION public.get_resumen_movimientos(p_codigo VARCHAR, p_limit INT DEFAULT 5)
RETURNS TABLE (
  fecha TEXT,
  tipo TEXT,
  cantidad NUMERIC,
  referencia TEXT
) AS $$
BEGIN
  RETURN QUERY
  (
    -- Ventas (Status = 1)
    SELECT 
      to_char(v.created_at, 'DD/MM/YYYY') as fecha,
      'venta'::TEXT as tipo,
      -(vd.cantidad)::NUMERIC as cantidad,
      ('Factura ' || COALESCE(vd.documento, ''))::TEXT as referencia
    FROM ventas_detalle vd
    JOIN ventas v ON vd.venta_id = v.id
    WHERE vd.codigo_producto = p_codigo AND v.status = 1
    
    UNION ALL
    
    -- Ajustes e Ingresos (Emitidos)
    SELECT 
      to_char(oc.creado_en, 'DD/MM/YYYY') as fecha,
      CASE WHEN oci.delta > 0 THEN 'ingreso' ELSE 'ajuste' END::TEXT as tipo,
      oci.delta::NUMERIC as cantidad,
      COALESCE(oc.nota, 'Ajuste')::TEXT as referencia
    FROM ordenes_cambio_items oci
    JOIN ordenes_cambio oc ON oci.orden_id = oc.id
    WHERE oci.codigo_producto = p_codigo AND oc.status = 'emitido'
  )
  ORDER BY fecha DESC
  LIMIT p_limit;
END;
$$ LANGUAGE plpgsql;

-- 12. Cola de atención: clientes que pidieron hablar con un empleado (escalación del bot).
--     El bot, al emitir [ESCALAR_HUMANO], inserta aquí (rol anon). La app de empleados se
--     suscribe por Realtime (rol authenticated) y muestra/atiende la cola.
CREATE TABLE IF NOT EXISTS public.atenciones_pendientes (
    id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    telefono     text NOT NULL,
    nombre       text,
    motivo       text,                                  -- último mensaje del cliente que disparó la escalación
    status       text NOT NULL DEFAULT 'pendiente',     -- 'pendiente' | 'atendido'
    creado_en    timestamp with time zone NOT NULL DEFAULT now(),
    atendido_en  timestamp with time zone,
    atendido_por uuid
);
COMMENT ON TABLE public.atenciones_pendientes IS
  'Cola de clientes en espera de atención de un empleado (escalación del bot). La app de empleados se suscribe por Realtime.';

-- Clave foránea a profiles (tabla del employee-app). Se aplica solo si ya existe.
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.tables
    WHERE table_schema = 'public' AND table_name = 'profiles'
  ) THEN
    BEGIN
      ALTER TABLE public.atenciones_pendientes
        ADD CONSTRAINT fk_atenciones_atendido_por
        FOREIGN KEY (atendido_por) REFERENCES public.profiles(id) ON DELETE SET NULL;
    EXCEPTION WHEN duplicate_object THEN NULL;
    END;
  END IF;
END $$;

-- A lo sumo UNA atención pendiente por teléfono (evita duplicados si el cliente reescala).
CREATE UNIQUE INDEX IF NOT EXISTS uniq_atenciones_tel_pendiente
  ON public.atenciones_pendientes (telefono) WHERE status = 'pendiente';
CREATE INDEX IF NOT EXISTS idx_atenciones_status_creado
  ON public.atenciones_pendientes (status, creado_en);

ALTER TABLE public.atenciones_pendientes ENABLE ROW LEVEL SECURITY;

-- anon (n8n / backend): leer (dedup) + insertar (encolar)
DROP POLICY IF EXISTS "n8n - select atenciones" ON public.atenciones_pendientes;
CREATE POLICY "n8n - select atenciones" ON public.atenciones_pendientes
  FOR SELECT TO anon USING (true);
DROP POLICY IF EXISTS "n8n - insert atenciones" ON public.atenciones_pendientes;
CREATE POLICY "n8n - insert atenciones" ON public.atenciones_pendientes
  FOR INSERT TO anon WITH CHECK (true);

-- Políticas para empleados: solo si is_active_employee() y validate_session() existen.
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM pg_proc p JOIN pg_namespace n ON p.pronamespace = n.oid
             WHERE n.nspname = 'public' AND p.proname = 'is_active_employee')
  AND EXISTS (SELECT 1 FROM pg_proc p JOIN pg_namespace n ON p.pronamespace = n.oid
              WHERE n.nspname = 'public' AND p.proname = 'validate_session')
  THEN
    DROP POLICY IF EXISTS "Empleados - leer atenciones" ON public.atenciones_pendientes;
    CREATE POLICY "Empleados - leer atenciones" ON public.atenciones_pendientes
      FOR SELECT TO authenticated USING (is_active_employee());
    DROP POLICY IF EXISTS "Empleados - actualizar atenciones" ON public.atenciones_pendientes;
    CREATE POLICY "Empleados - actualizar atenciones" ON public.atenciones_pendientes
      FOR UPDATE TO authenticated USING (is_active_employee() AND validate_session())
      WITH CHECK (is_active_employee() AND validate_session());
  ELSE
    RAISE NOTICE 'Saltando políticas de empleados para atenciones_pendientes: is_active_employee() o validate_session() no existen todavía.';
  END IF;
END $$;

-- Realtime: la app recibe el aviso al instante.
ALTER TABLE public.atenciones_pendientes REPLICA IDENTITY FULL;
DO $$
BEGIN
  BEGIN
    ALTER PUBLICATION supabase_realtime ADD TABLE public.atenciones_pendientes;
  EXCEPTION
    WHEN duplicate_object THEN NULL;
    WHEN others THEN RAISE NOTICE 'no se pudo agregar a supabase_realtime: %', SQLERRM;
  END;
END $$;

-- 13. Solicitudes de ayuda: el bot no encontró algo o el cliente refutó el resultado.
--     El bot inserta (anon) al emitir [PEDIR_AYUDA]; la app de empleados elige los productos
--     (solicitudes_ayuda_items) y marca 'resuelto'; el webhook n8n /reenviar-ayuda arma y reenvía
--     el mensaje al cliente y marca 'enviado'.
CREATE TABLE IF NOT EXISTS public.solicitudes_ayuda (
    id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    telefono     text NOT NULL,
    nombre       text,
    consulta     text,                                  -- lo que pidió el cliente (su mensaje / lista)
