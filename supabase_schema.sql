-- 6. Tablas de transacciones de ventas
CREATE TABLE IF NOT EXISTS public.ventas (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    documento text,
    fecha_emision date,
    rif_cliente text,
    total_neto numeric,
    total_impuesto numeric,
